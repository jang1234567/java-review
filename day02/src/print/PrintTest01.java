package print;

public class PrintTest01 {	//PrintTest01 클래스의 시작	영역
	public static void main(String[] args) {	//메인메소드의 시작영역
		System.out.println("출력메소드 연습");
//		출력메소드 연습 출력후 줄바꿈;
		System.out.print("print()메소드는 ");
//		print()메소드는 출력;
		System.out.print("줄바꿈이 되지 않는다.\n");
//		줄바꿈이 되지 않는다.\n(줄바꿈) 출력;
		System.out.println("println() 메소드는 줄바꿈이 된다.");
//		println() 메소드는 줄바꿈이 된다. 출력후 줄바꿈;
		
	}//메인메소드의 끝 영역

}	//PrintTest01 클래스의 끝 영역
