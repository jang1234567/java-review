package variable;

public class Variable01 {	//Variable01 클래스의 시작영역
	public static void main(String[] args) {	//메인메소드의 시작영역
		int num;	//num이라는 정수형 변수 선언;
		num = 10;	//정수형 변수 num값에 10을 대입;
		int num2 = 20;	//num2라는 정수형 변수 선언, num2에 20 대입;
		System.out.println(num);	// num값 콘솔창에 출력후 줄바꿈;
		System.out.println(num2);	// num2값 콘솔창에 출력후 줄바꿈;

		int number1 = 0, number2 = 100, number3 = 200;
		//정수형 변수 number1 선언후 0대입, 정수형 변수 number2 선언후 100대입, 정수형 변수 number3 선언후 200대입;
		System.out.println(number1);	//number1값 콘솔창에 출력후 줄바꿈
		System.out.println(number2);	//number2값 콘솔창에 출력후 줄바꿈
		System.out.println(number3);	//number3값 콘솔창에 출력후 줄바꿈
		//자동정렬 : ctrl + shift + f
		//한줄복사 : ctrl + alt 방향키 위아래
		//여러줄 복사 : shift + 방향기 범위 지정 -> ctrl + alt + 방향키 위아래

	}//메인메소드의 끝 영역
}	//Variable01 클래스의 끝 영역
