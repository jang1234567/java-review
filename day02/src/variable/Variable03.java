package variable;

public class Variable03 { //Variable03 클래스의 시작 영역
	public static void main(String[] args) {	//메인 메소드의 시작영역
		String name = "장윤근";	//문자열 변수 name 선언후 장윤근이라는 문자열 대입;
		int age = 26;	//정수형 변수 age 선언후 26이라는 정수값 대입;
		System.out.println(name);	//name값을 콘솔창에 출력후 줄바꿈;
		System.out.println(age);	//age값을 콘솔창에 출력후 줄바꿈;
		
		// 제 이름은 장윤근이고 제 나이는 20살 입니다
		System.out.println("제 이름은 " + name + "이고 제 나이는 " + age + "살 입니다");
		
	
	}	//메인메소드의 끝 영역
}//Variable03 클래스의 끝 영역
