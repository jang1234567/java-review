package variable;

public class Variable02 { //Variable02 클래스의 시작영역
	public static void main(String[] args) {	//메인메소드 시작 영역
		int number = 1;
		//정수형 number 변수를 선언하고 1이라는 정수값으로 초기화(대입);
		
		System.out.println(number); //number의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(number + 10);	//number + 10의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(number + 20);	//number + 20의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(number + 30);	//number + 30의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(number + 40);	//number + 40의 값을 콘솔창에 출력후 줄바꿈;
		
		int userAge = 26;	//정수형 변수 userAge 선언하고 26 정수값으로 초기화;
		String userName = "장윤근";	//문자열 변수 userName 선언하고 장윤근 문자열로 초기화;
		double date = 3.11;	//실수형 변수 date 선언하고 실수값 3.11 초기화;
		boolean isTrue = true;	// 논리형 변수 isTrue 선언하고 논리값 true 초기화;
		char gender = 'm';	//문자형 변수 gender 선언하고 m 문자형으로 초기화
		
		System.out.println(userName);	//userName의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(userAge);	//userAge의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(date);	//data의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(isTrue);	//isTrue의 값을 콘솔창에 출력후 줄바꿈;
		System.out.println(gender);	//gender의 값을 콘솔창에 출력후 줄바꿈;
		
		
	}	//메인메소드 끝 영역

}	//Variable02 클래스의 끝 영역
