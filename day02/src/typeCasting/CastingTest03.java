package typeCasting;

public class CastingTest03 {	//CastingTest03클래스의 시작 영역
	public static void main(String[] args) {	//메인메소드의 시작 영역
		//유니코드 값 확인
		char ch1 = 'A';	// 문자형 변수 ch1 선언후 A대입;
		char ch2 = 'a';	//문자형 변수 ch2 선언후 a 대입;
		System.out.println(ch1); //ch1값 출력후 줄바꿈;
		System.out.println(ch2);	//ch2값 출력후 줄바꿈;
		char ch3 = 65;	//'A'	//문자형 변수 ch3 선언후 65대입;
		char ch4 = 97;	//'a'	// 문자형 변수 ch4 선언후 97 대입;
		System.out.println(ch3);	//ch3값 출력후 줄바꿈;
		System.out.println(ch4);	//ch4값 출력후 줄바꿈;
	}	//메인메소드의 끝 영역

}	//CastingTest03클래스의 끝 영역
